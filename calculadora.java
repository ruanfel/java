
class Calculadora {
    public static float Somar(float a, float b) {

        return a + b;
    }

    public static float Subtrair(float a, float b) {

        return a - b;
    }

    public static float Multiplicar(float a, float b) {

        return a * b;
    }

    public static float dividir(float a, float b) {

        return a / b;

    }

}